export const generateFancyFonts = (text: string) => {
  return Object.entries(maps).map(([name, map]) => {
    let result = '';
    for (const char of text) {
      const index = alphabet.indexOf(char);
      if (index !== -1) {
        // High surrogates logic for characters outside BMP
        const mapped = Array.from(map);
        result += mapped[index];
      } else {
        result += char;
      }
    }
    return { name: name.replace(/([A-Z])/g, ' $1').replace(/^./, str => str.toUpperCase()), text: result };
  });
};
